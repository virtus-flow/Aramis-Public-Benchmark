def run_statistical_forensics(df_sub):
    print("\n" + "="*75)
    print("        PHM STATISTICAL REPORT - RIGOROUS WITHIN-SYSTEM VALIDATION      ")
    print("="*75)

    intra_system_intervals = []

    # Track cascading intervals strictly WITHIN each distinct coupled machine asset
    for m in range(1, 51):
        system_data = df_sub[df_sub['m'] == m]
        valid_tau = system_data['tau_hat'].dropna().values

        if len(valid_tau) > 1:
            sorted_tau = np.sort(valid_tau)
            diffs = np.diff(sorted_tau)

            # Isolate positive continuous increments.
            # (Simultaneous step jumps represent common-cause Merton shocks)
            positive_diffs = diffs[diffs > 0]
            intra_system_intervals.extend(positive_diffs)

    intra_system_intervals = np.array(intra_system_intervals)

    if len(intra_system_intervals) > 5:
        # Non-parametric Kolmogorov-Smirnov comparison against memoryless Poisson CDF
        ks_stat, p_ks = stats.kstest(intra_system_intervals, 'expon', args=(0, np.mean(intra_system_intervals)))

        print(f"Total intra-system continuous cascading intervals evaluated: {len(intra_system_intervals)}")
        print(f"Mean cascade propagation delta: {np.mean(intra_system_intervals):.2f} atu")
        print(f"Corrected Kolmogorov-Smirnov test: KS = {ks_stat:.4f} | p-value = {p_ks:e}")

        if p_ks < 0.001:
            print("Status: Pure Poisson independence REJECTED at alpha=0.001.")
            print("Causal cross-excitation cascade mechanics empirically validated.")
        else:
            print("Status: Failed to reject memoryless baseline regime.")
    else:
        print("[WARNING] Insufficient multi-component failure intervals to calculate ECDF.")
    print("="*75 + "\n")

