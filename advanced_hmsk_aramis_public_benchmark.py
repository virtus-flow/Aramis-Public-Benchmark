# 3. PRODUCTION PIPELINE FOR DATASET PARSING & SUBMISSION GENERATION
# ==============================================================================
def execute_aramis_pipeline(mat_file_path, output_csv_path):
    """ Parses raw .mat stream, runs the Alfa-Pulse v10 engine, and writes standard CSV """
    if not os.path.exists(mat_file_path):
        print(f"[ERROR] File {mat_file_path} not found. Ensure it is uploaded to Colab environment.")
        return

    print(f"[INFO] Ingesting raw binary MAT-stream from storage: {mat_file_path}...")

    # Isključena striktna provera integriteta kompresije radi zaobilaženja OSError zlib strimovanja
    try:
        mat_contents = sio.loadmat(mat_file_path, squeeze_me=False, verify_compressed_data_integrity=False)
    except TypeError:
        # Za starije verzije scipy paketa koje nemaju ovaj parametar
        mat_contents = sio.loadmat(mat_file_path, squeeze_me=False)

    data_keys = [k for k in mat_contents.keys() if not k.startswith('__')]
    data_key = data_keys[0]
    raw_data = mat_contents[data_key]

    if raw_data.shape != (1, 50):
        print(f"[WARNING] Shape mismatch (1,50) expected, got {raw_data.shape}. Attempting recovery...")

    M_test = raw_data.shape[1]
    print(f"[INFO] Total testing independent sub-fleets detected: {M_test}")

    J_components = 4
    K_sensors = 10
    submission_rows = []
    n_row_index = 1

    for m in range(M_test):
        struct = raw_data[0, m]

        try:
            normalized_components = extract_and_transpose_system(struct)
        except Exception as e:
            print(f"[WARNING] Fleet system {m+1}: Ingestion failed. Error: {e}. Skipping fleet.")
            continue

        valid = True
        T_steps = None
        for j in range(J_components):
            mat = normalized_components[j]
            if mat.ndim != 2 or mat.shape[1] != K_sensors:
                print(f"[WARNING] Fleet {m+1}, Component {j+1} has invalid dimensions {mat.shape}. Skipping.")
                valid = False
                break
            if T_steps is None:
                T_steps = mat.shape[0]
            elif mat.shape[0] != T_steps:
                print(f"[WARNING] Fleet {m+1}: Non-synchronized matrix temporal lengths. Skipping.")
                valid = False
                break

        if not valid:
            continue

        # Instantiate raw kernel pipeline
        kernel = AramisAlfaPulseKernel(fs=1.0, n_components=J_components, n_sensors=K_sensors)

        detected_tau = np.ones(J_components) * np.nan
        resolved_components = np.zeros(J_components, dtype=bool)

        for t in range(T_steps):
            X_t = np.zeros((J_components, K_sensors))
            for j in range(J_components):
                X_t[j, :] = normalized_components[j][t, :]

            _, triggers = kernel.process_time_step(X_t)

            for j in range(J_components):
                if triggers[j] and not resolved_components[j]:
                    detected_tau[j] = float(t)
                    resolved_components[j] = True

            if np.all(resolved_components):
                break

        print(f"[INFO] Fleet Asset {m+1} processed successfully. Evaluated Tau vectors: {detected_tau}")

        for j in range(J_components):
            submission_rows.append({
                'n_row': n_row_index,
                'm': m + 1,
                'j': j + 1,
                'tau_hat': detected_tau[j]
            })
            n_row_index += 1

    if not submission_rows:
        print("[ERROR] Processing cycle failed across all elements. Output is empty.")
        df_submission = pd.DataFrame(columns=['n_row', 'm', 'j', 'tau_hat'])
    else:
        df_submission = pd.DataFrame(submission_rows)

    df_submission.to_csv(output_csv_path, index=False, header=True)
    print(f"[SUCCESS] Official challenge submission file written to: {output_csv_path}")
    print("\nPreview (First 8 evaluation boundaries rows):")
    print(df_submission.head(8).to_string(index=False))

