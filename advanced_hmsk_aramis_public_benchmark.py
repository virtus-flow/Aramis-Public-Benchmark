# 1. MULTIVARIATE ALFA-PULSE KERNEL V10 (ARAMIS COUPLING OPTIMIZED)
# ==============================================================================
class AramisAlfaPulseKernel:
    """
    Advanced Alfa-Pulse Kernel v10 engineered for the Aramis Challenge.
    Features a parallel Welford moving window tracker for K=10 sensors and
    stochastic cross-excitation (Hawkes coupling) for J=4 interconnected components.
    """
    def __init__(self, fs=1.0, n_components=4, n_sensors=10):
        self.J = n_components
        self.K = n_sensors
        self.dt = 1.0 / fs

        # Stochastic intensity hyperparameters (Calibrated on Training Fleet M=200)
        self.beta = 0.12        # Intensity exponential recovery rate
        self.mu = 0.15          # Baseline intensity under normal conditions
        self.kick = 0.025       # Merton Jump enhancement upon anomaly detection

        # Hawkes Cross-Excitation Matrix (Endogenous cascading damage propagation)
        self.gamma = np.array([
            [0.00, 0.05, 0.05, 0.05],
            [0.05, 0.00, 0.05, 0.05],
            [0.05, 0.05, 0.00, 0.05],
            [0.05, 0.05, 0.05, 0.00]
        ])

        # Initialize hazard intensity vectors for all J components
        self.lambda_vector = np.ones(self.J) * self.mu

        # Multivariate Welford accumulators: [component, sensor]
        self.running_mean = np.zeros((self.J, self.K))
        self.running_M2 = np.zeros((self.J, self.K))
        self.sample_count = np.zeros(self.J, dtype=int)

    def process_time_step(self, X_t):
        """
        Processes a single multi-channel stream step (Matrix slice of size J x K).
        X_t: snapshot matrix (4, 10) representing components (rows) and sensors (columns).
        """
        tau_hat_triggered = np.zeros(self.J, dtype=bool)

        for j in range(self.J):
            self.sample_count[j] += 1

            # Moving window reset interval to prevent variance saturation under long cycles
            if self.sample_count[j] > 50:
                self.sample_count[j] = 1
                self.running_M2[j, :] = 0.05

            # Compute parallel vectorized Welford update across all K sensors simultaneously
            sensors_sample = X_t[j, :]
            delta = sensors_sample - self.running_mean[j, :]
            self.running_mean[j, :] += delta / self.sample_count[j]
            delta2 = sensors_sample - self.running_mean[j, :]
            self.running_M2[j, :] += delta * delta2

            stdev = np.sqrt(self.running_M2[j, :] / self.sample_count[j]) + 1e-6
            adaptive_threshold = self.running_mean[j, :] + 3.0 * stdev

            # Execute baseline continuous exponential recovery for component j
            self.lambda_vector[j] += self.beta * (self.mu - self.lambda_vector[j]) * self.dt

            # Verify if any of the K sensors breach their local adaptive 3-Sigma bound
            anomalous_sensors = np.where(sensors_sample > adaptive_threshold)[0]

            if len(anomalous_sensors) > 0:
                # Quantify Merton Jump magnitude ratio and execute intensity kick
                magnitude_ratio = np.max(sensors_sample / (adaptive_threshold + 1e-6))
                self.lambda_vector[j] += self.kick * (magnitude_ratio ** 2)

                # Broadcast Hawkes cross-excitation intensity cascade to interconnected assets
                for i in range(self.J):
                    if i != j:
                        self.lambda_vector[i] += self.gamma[i, j] * self.kick

            # Critical state assessment: check if hazard intensity breaches degradation threshold
            if self.lambda_vector[j] > 0.85:
                tau_hat_triggered[j] = True

        # Vectorized intensity clipping for stream numerical stabilization
        self.lambda_vector = np.clip(self.lambda_vector, 0.15, 1.5)

        return self.lambda_vector.copy(), tau_hat_triggered

