# 2. BINARY MAT-STREAM PARSER AND DATA TRANSFORMATION
# ==============================================================================
def extract_and_transpose_system(raw_element):
    """
    Extracts trajectory from void structure and reshapes to structured formats.
    trajectory: (1,4) array, each element maps to (10, T).
    Returns list of 4 matrices shaped as (T, 10).
    """
    if not isinstance(raw_element, np.void):
        raise ValueError("Expected struct data layout (numpy.void).")

    elem_dict = {name: raw_element[name] for name in raw_element.dtype.names}

    if 'trajectory' not in elem_dict:
        raise ValueError("Structure lacks the required 'trajectory' field.")

    traj = elem_dict['trajectory']

    if traj.shape != (1, 4):
        traj = traj.flatten()
        if len(traj) != 4:
            raise ValueError(f"Unexpected trajectory matrix dimensions: {traj.shape}")
    else:
        traj = traj[0]

    components = []
    for j in range(4):
        mat = traj[j]
        if mat.ndim == 2 and mat.shape[0] == 10:
            components.append(mat.T)
        elif mat.ndim == 2 and mat.shape[1] == 10:
            components.append(mat)
        else:
            raise ValueError(f"Component {j} exhibits incompatible dimensions: {mat.shape}")

    return components

